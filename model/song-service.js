
class Song {
    constructor(id, nome, artista, album, tamanho, duracao, rota) {
        this.id = id;
        this.nome = nome;
        this.artista = artista;
        this.album = album;
        this.tamanho = tamanho;
        this.duracao = duracao;
        this.rota = rota;
    }
}

module.exports = Song;
